package br.com.senaisp.bauru.richard.licao03;

public class Parsing01 {

	public static void main(String[] args) {
		String precoCamiseta = "15";
		String percentualImposto = "0.05";
		String hash = "887ds7nds87dsfs";
		
		System.out.println(Integer.parseInt(precoCamiseta));
		System.out.println(Double.parseDouble(percentualImposto));
		
		System.out.println(Integer.parseInt(hash));
	}

}
